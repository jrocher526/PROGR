
# Se solicita a una persona 2 numeros.
n1 = input("Introduce un numero:")
n2 = input("Introduce otro numero para sumarlos:")

# Se hace una conversion de string a int
n1 = int(n1)
n2 = int(n2)

# Se realiza la suma de ambos numeros
suma = n1 + n2

# Se muestra por pantalla el resultado
print("La suma de los dos numeros es:", suma)
